// 分割統治法を使用して最近2点対を求める
// 点の個数をNとするとO(NlogN)
// dによる制約で分割の間は最大で6個の頂点間のみの探索
typedef pair<ld, ld> P;
bool compare_y(P a, P b) {
  return a.second < b.second;
};
// (s, e]
ld closest_pair(vector<P> &A, int s, int e) {
  int n = e-s;
  if (n <= 1) return (ld)LINF;
  int m = n/2;
  ld x = A[s+m].first;
  ld d = min<ld>(closest_pair(A, s, s+m), closest_pair(A, s+m, e)); // divide and recurrent
  inplace_merge(A.begin()+s, A.begin()+s+m, A.begin()+e, compare_y); // merge 2 sorted array

  // now A is y ascending order order

  vector<P> B; // store nodes which distance is less than d
  for (int i = 0; i < n; i++) {
    if (fabsl(A[s+i].first - x) >= d) continue;

    // see b's node from tail to diff with y is more than d
    for (int j = 0; j < B.size(); j++) {
      ld dx = A[s+i].first - B[B.size() - j - 1].first;
      ld dy = A[s+i].second - B[B.size() - j - 1].second;
      if (dy >= d) break;
      d = min<ld>(d, sqrtl(dx*dx+dy*dy));
    }
    B.push_back(A[s+i]);
  }

  return d;
};
